/**
 * Таблица транслитерации: латиница → кириллица
 * Охватывает русский алфавит + таджикские буквы
 */
const LAT_TO_CYR: [string, string][] = [
  // Двухсимвольные сначала (порядок важен)
  ['zh', 'ж'], ['ZH', 'Ж'], ['Zh', 'Ж'],
  ['ch', 'ч'], ['CH', 'Ч'], ['Ch', 'Ч'],
  ['sh', 'ш'], ['SH', 'Ш'], ['Sh', 'Ш'],
  ['kh', 'х'], ['KH', 'Х'], ['Kh', 'Х'],
  ['ts', 'ц'], ['TS', 'Ц'], ['Ts', 'Ц'],
  ['yu', 'ю'], ['YU', 'Ю'], ['Yu', 'Ю'],
  ['ya', 'я'], ['YA', 'Я'], ['Ya', 'Я'],
  ['yo', 'ё'], ['YO', 'Ё'], ['Yo', 'Ё'],
  // Односимвольные
  ['a', 'а'], ['A', 'А'],
  ['b', 'б'], ['B', 'Б'],
  ['v', 'в'], ['V', 'В'],
  ['g', 'г'], ['G', 'Г'],
  ['d', 'д'], ['D', 'Д'],
  ['e', 'е'], ['E', 'Е'],
  ['z', 'з'], ['Z', 'З'],
  ['i', 'и'], ['I', 'И'],
  ['j', 'й'], ['J', 'Й'],
  ['k', 'к'], ['K', 'К'],
  ['l', 'л'], ['L', 'Л'],
  ['m', 'м'], ['M', 'М'],
  ['n', 'н'], ['N', 'Н'],
  ['o', 'о'], ['O', 'О'],
  ['p', 'п'], ['P', 'П'],
  ['r', 'р'], ['R', 'Р'],
  ['s', 'с'], ['S', 'С'],
  ['t', 'т'], ['T', 'Т'],
  ['u', 'у'], ['U', 'У'],
  ['f', 'ф'], ['F', 'Ф'],
  ['h', 'ҳ'], ['H', 'Ҳ'],
  ['c', 'к'], ['C', 'К'],
  ['x', 'ҳ'], ['X', 'Ҳ'],
  ['y', 'й'], ['Y', 'Й'],
  ['q', 'қ'], ['Q', 'Қ'],
  ['w', 'в'], ['W', 'В'],
];

/**
 * Конвертирует латинский текст в кириллический эквивалент.
 * Если текст уже кириллица — возвращает его без изменений.
 */
export function latinToCyrillic(text: string): string {
  if (!text) return text;
  // Если в строке нет латинских букв — возвращаем как есть
  if (!/[a-zA-Z]/.test(text)) return text;

  let result = text;
  for (const [lat, cyr] of LAT_TO_CYR) {
    result = result.split(lat).join(cyr);
  }
  return result;
}

/**
 * Возвращает массив вариантов поискового запроса:
 * оригинал + кириллический вариант (если отличается)
 */
export function searchVariants(query: string): string[] {
  if (!query) return [query];
  const cyrVariant = latinToCyrillic(query);
  if (cyrVariant !== query) {
    return [query, cyrVariant];
  }
  return [query];
}
