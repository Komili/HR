/**
 * Конвертирует латинский текст в кириллический эквивалент (для поиска).
 * Если текст уже кириллица — возвращает его без изменений.
 */
export function latinToCyrillic(text: string): string {
  if (!text) return text;
  if (!/[a-zA-Z]/.test(text)) return text;

  let result = text;
  for (const [lat, cyr] of LAT_TO_CYR) {
    result = result.split(lat).join(cyr);
  }
  return result;
}

/**
 * Возвращает массив вариантов поискового запроса:
 * оригинал + кириллический вариант (если отличается)
 */
export function searchVariants(query: string): string[] {
  if (!query) return [query];
  const cyrVariant = latinToCyrillic(query);
  if (cyrVariant !== query) {
    return [query, cyrVariant];
  }
  return [query];
}
